%G-S正交化
function A = ThreeGS(V)  % V 为3*3向量
v1 = V(:,1);
v2 = V(:,2);
v3 = V(:,3);
a1 = zeros(3,1);
a2 = zeros(3,1);
a3 = zeros(3,1);
%=====以下部分防止相量等于0  开始==========
if(v1==0)
    v1=[0.001,0,0];
end;
if(v2==0)
    v2=[0,0.001,0];
end;
if(v3==0)
    v3=[0,0,0.001];
end;
%=====以下部分防止相量等于0  结束==========
%=====以下部分为相量正交化 开始==============
a1 = v1;
a2 = v2-((a1'*v2)/(a1'*a1))*a1;
a3 = v3-((a1'*v3)/(a1'*a1))*a1-((a2'*v3)/(a2'*a2))*a2;
%=====以下部分为相量正交化 结束==============
A = [a1,a2,a3];
end
