%------------------------------------------------------------------------------------
%  坐标控制
%------------------------------------- -----------------------------------------------

%{
在绘制图形时，MATLAB可以自动根据要绘制曲线数据的范围选择合适的坐标刻度，使得曲线能够尽可能清晰地显示出来。
所以，在一般情况下用户不必选择坐标轴的刻度范围。但是，如果用户对坐标系不满意，可利用axis函数对其重新设定。
该函数的调用格式如下：axis（[xmin，xmax，ymin，ymax，zmin，zmax]）
如果只给出前4个参数，则MATLAB按照给出的x、y轴的最小值和最大值选择坐标系范围，以便绘制出合适的二维曲线。
如果给出了全部参数，则系统按照给出的3个坐标轴的最小值和最大值选择坐标系范围，以便绘制出合适的三维图形。
axis函数功能丰富，常用的用法还有以下几种。
（1）axis equal：纵、横坐标轴采用等长刻度。
（2）axis square：产生正方形坐标系（默认为矩形）。
（3）axis auto：使用默认设置。
（4）axis off：取消坐标轴。
（5）axis on：显示坐标轴。
    给坐标加网格线用grid命令来控制。
        grid on/off命令控制是画还是不画网格线，不带参数的grid命令在两种状态之间进行切换。
    给坐标加边框用box命令来控制。
        box on/off命令控制是加还是不加边框线，不带参数的box命令在两种状态之间进行切换。
    一般情况下，每执行一次绘图命令就刷新一次当前图形窗口，图形窗口原有图形将不复存在。
        若希望在已存在的图形上再继续添加新的图形，可使用图形保持命令hold。
        hold on/off 命令控制是保持原有图形还是刷新原有图形，不带参数的hold命令在两种状态之间进行切换。
%}




