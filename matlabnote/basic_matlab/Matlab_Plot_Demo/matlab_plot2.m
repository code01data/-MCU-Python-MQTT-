%{
   peaks 是从高斯分布转换和缩放得来的包含两个变量的函数，在演示 mesh、surf、pcolor、contour 等函数中很有用。
    Z = peaks; 返回一个 49×49 矩阵。
    Z = peaks(n); 返回一个 n×n 矩阵。
    Z = peaks(V); 返回一个 n×n 矩阵，其中 n = length(V)。
    Z = peaks(X,Y); 在给定的 X 和 Y（必须大小相同）处计算 peaks 并返回大小相同的矩阵。
    peaks(...)（无输出参数）使用 surf 绘制 peaks 函数。使用先前语法中的任意输入参数组合。
    [X,Y,Z] = peaks(...); 返回另外两个矩阵 X 和 Y 用于参数绘图，例如 surf(X,Y,Z,del2(Z))。如未作为输入参数给出，基础矩阵 X 和 Y 是
    [X,Y] = meshgrid(V,V) 
    其中 V 是给定向量，或者 V 是长度为 n 的向量，其元素从 -3 到 3 均匀间隔。如果未给出输入参数，默认的 n 是 49。
%}

%------------------------------------------------------------------------------------
%  Contour() 绘制曲面的等高线图(矩阵的等高线图)
%------------------------------------- -----------------------------------------------

%{
    contour(Z) 创建一个包含矩阵 Z 的等值线的等高线图，其中 Z 包含 x-y 平面上的高度值。MATLAB会自动选择要显示的等高线. Z的列和行索引分别是平面中的 x 和 y 坐标。
    contour(X,Y,Z),(x,y)是z=0上面的点的坐标矩阵,z为相应点的高度值矩阵，绘制等高线。
    contour(z,n) 绘制矩阵z的n个等高线。contour(x,y,z,n) 绘制矩阵z的n个等高线。
    contour(z,v) 在指定高度画出等高线,其中v为向量,画出length(z)条线，每条高度为v(i).
    contour(___,levels) 将要显示的等高线指定为上述任一语法中的最后一个参数。
        将 levels 指定为标量值 n，以在 n 个自动选择的层级（高度）上显示等高线。要在某些特定高度绘制等高线，请将 levels 指定为单调递增值的向量。
        要在一个高度 (k) 绘制等高线，请将 levels 指定为二元素行向量 [k k]。
    contour(___,LineSpec) 指定等高线的线型和颜色。
    contour(___,Name,Value) 使用一个或多个名称-值对组参数指定等高线图的其他选项。请在所有其他输入参数之后指定这些选项。有关属性列表，请参阅 Contour 属性。
    contour(ax,___) 在目标坐标区中显示等高线图。将坐标区指定为上述任一语法中的第一个参数。
    [M,c] = contour(___) 返回等高线矩阵和等高线对象 c。显示等高线图后，使用 c 设置属性。
%}

% 函数的等高线
contour_x = linspace(-2*pi,2*pi);
contour_y = linspace(0,4*pi);
[X1,Y1] = meshgrid(contour_x,contour_y);
contour_Z = sin(X1)+cos(Y1);
figure
contour(X1,Y1,contour_Z)

% Z = peaks(n); 返回一个 n×n 矩阵。
peaks_z = peaks(50);
figure
contour(peaks_z)


% 将 Z 定义为两个变量 X 和 Y 的函数。然后创建该函数的等高线图，并通过将 ShowText 属性设置为 'on' 来显示标签
% meshgrid 函数用来生成网格矩阵，可以是二维网格矩阵
%[X,Y] = meshgrid(x,y) 基于向量 x 和 y 中包含的坐标返回二维网格坐标。X 是一个矩阵，每一行是 x 的一个副本；
%       Y 也是一个矩阵，每一列是 y 的一个副本。坐标 X 和 Y 表示的网格有 length(y) 个行和 length(x) 个列。
contour_x1 = -2:0.2:2;
contour_y1 = -2:0.2:3;

[contour_X1,contour_Y1] = meshgrid(contour_x1,contour_y1); %函数用来生成网格矩阵，可以是二维网格矩阵
 
contour_Z1 = contour_X1.*exp(-contour_X1.^2-contour_Y1.^2);%生成新的函数
figure
contour(contour_X1,contour_Y1,contour_Z1,'ShowText','on')


%subplot(m,n,p) 会将图窗窗口划分为由多个小子图组成的 m×n 矩阵，并选择第 p 个子图作为当前绘图。
% 这些绘图沿图窗窗口的第一行进行编号，然后沿第二行进行编号，依此类推。
x = 0:pi/20:2*pi;
subplot(3,1,1); plot(sin(x))
subplot(3,1,2); plot(cos(x))
subplot(3,1,3); plot(sin(x).*cos(x))



[clabel_x,clabel_y,clabel_z] = peaks;
figure
[clabel_C,clabel_h] = contour(clabel_x,clabel_y,clabel_z);   
clabel(clabel_C,clabel_h,'FontSize',15,'Color','red')  %创建一个等高线图并获取等高线矩阵 C 和等高线对象 h。然后，为等高线图添加标签。

