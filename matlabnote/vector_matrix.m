% 使用“[]” 抽取向量内容
A1=[1,2,3,5,6,8];
B1=A1([1 3 5])   % [1 3 5]表示提取第1，3，5个元素

% 使用“ 抽取向量” 抽取其他的向量内容
% 抽取向量 a=[1 3 5] [1 3 5]表示提取第1，3，5个元素
a=[1 3 5];
A2=[4,2,3,6,9,8];
B2=A2(a)
% 使用“:” 抽取向量内容
A3=[1,2,3,5,6,8];
B3=A3(1:2:6)  

%(1:2:6)  表示在1~6范围内，每隔2进行取数
%(m:d:n)  表示在m~n范围内，每隔d进行取数(d步长,间距大小)


