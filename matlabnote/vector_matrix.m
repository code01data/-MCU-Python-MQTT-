%   logx1 = logspace(a, b)    在(a,b)之间生成50个线性分布的数据 构成等比向量  x(1)=10^a x(50)=10^b
%   logx2 = logspace(a, b,n)  在(a,b)之间生成n个线性分布的数据 构成等比向量   x(1)=10^a x(n)=10^b
%--------------------------------------------------------------------------
