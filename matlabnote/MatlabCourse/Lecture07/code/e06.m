%% 一元多项式回归
x0=17:2:29;x0=[x0,x0];
y0=[20.48 25.13 26.15 30.0 26.1 20.3 19.35 24.35 28.11 26.3 31.4 26.92 25.7 21.3];
%% 绘制曲线图观察
plot(x0,y0,'r*');
%% 有曲线发现是较明显的拱形，可假设为二次函数关系式
[p,s]=polyfit(x0,y0,2);
p %输出结果是计算得到的拟合参数
[y,delta]=polyconf(p,x0,s)% 获取y的拟合值和预测值的置信区间半径

%% 使用polytool函数来可视化拟合曲线及其拟合半径，并可以获取预测值
polytool(x0,y0,2); 